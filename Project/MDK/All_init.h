#ifndef _ALL_INIT_H_
#define _ALL_INIT_H_

#include "headfile.h"
//====================================================宏定义函数区====================================================
//-------------------------------------------------------------------------------------------------------------------
// @brief       绝对值函数 数据范围是 [-32767,32767]
// @param       dat             需要求绝对值的数
// @return      int             返回绝对值
// Sample usage:                dat = myabs(dat);                               // 将dat变成正数
//-------------------------------------------------------------------------------------------------------------------
#define     myabs(x)            ((x)>=0? (x): (-(x)))

//-------------------------------------------------------------------------------------------------------------------
// @brief       限幅 数据范围是 [-32768,32767]
// @param       x               被限幅的数据
// @param       y               限幅范围(数据会被限制在-y至+y之间)
// @return      int             限幅之后的数据
// Sample usage:                int dat = limit(500,300);                       // 数据被限制在-300至+300之间  因此返回的结果是300
//-------------------------------------------------------------------------------------------------------------------
#define     limit(x,y)          ((x)>(y)? (y): ((x)<-(y)? (-(y)): (x)))

//-------------------------------------------------------------------------------------------------------------------
// @brief       双边限幅 数据范围是 [-32768,32767]
// @param       x               被限幅的数据
// @param       a               限幅范围左边界
// @param       b               限幅范围右边界
// @return      int             限幅之后的数据
// Sample usage:                int dat = limit_ab(500,-300,400);               //数据被限制在-300至+400之间  因此返回的结果是400
//-------------------------------------------------------------------------------------------------------------------
#define     limit_ab(x,a,b)     (((x)<(a))?(a):(((x)>(b))?(b):(x)))
//-------------------------------------------------------------------------------------------------------------------
// @brief       取两个数最大值最小值 数据范围是 [-32768,32767]
// @param       a               数据a
// @param       b               数据b
// @return      int             限幅之后的数据
// Sample usage:                int dat = max_ab(300,400)    //dat=400
//-------------------------------------------------------------------------------------------------------------------
#define     max_ab(a,b)         (((a)>=(b))? (a):(b))
#define     min_ab(a,b)         (((a)>=(b))? (b):(a))
//-------------------------------------------------------------------------------------------------------------------
// @brief       取两个数绝对值最大值最小值 数据范围是 [-32768,32767]
// @param       a               数据a
// @param       b               数据b
// @return      int             限幅之后的数据
// Sample usage:                int dat = limit_ab(500,-300,400);               //数据被限制在-300至+400之间  因此返回的结果是400
//-------------------------------------------------------------------------------------------------------------------
#define     max_abs_ab(a,b)         ((myabs((a))>=myabs((b)))? (a):(b))
#define     min_abs_ab(a,b)         ((myabs((a))<=myabs((b)))? (a):(b))

void All_init(void);

#endif
